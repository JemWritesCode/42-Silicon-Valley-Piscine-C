int		ft_atoi(char *str)
{
	long	res;
	int		i;
	int		neg;

	i = 0;
	neg = 1;
	res = 0;
	while ((str[i] == '\n' || str[i] == '\t' || str[i] == '\f' ||
			str[i] == '\r' || str[i] == ' ' || str[i] == '\v') && str[i])
		i++;
	if (str[i] == 43 || str[i] == 45)
	{
		if (str[i] == 45)
			neg = -1;
		i++;
	}
	while (str[i] >= 48 && str[i] <= 57 && str[i])
	{
		res = res * 10 + str[i] - 48;
		i++;
	}
	return ((int)res * neg);
}
